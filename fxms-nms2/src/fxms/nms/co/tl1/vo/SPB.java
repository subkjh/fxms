package fxms.nms.co.tl1.vo;

/**
 * Staging Parameter Blocks
 * @author subkjh
 *
 */
public class SPB {
	
	private String aid;
	private String ct;
	private String gb;
	private String tid;
	
	public String getAid() {
		return aid;
	}
	public String getCt() {
		return ct;
	}
	public String getGb() {
		return gb;
	}
	public String getTid() {
		return tid;
	}
	public void setAid(String aid) {
		this.aid = aid;
	}
	public void setCt(String ct) {
		this.ct = ct;
	}
	public void setGb(String gb) {
		this.gb = gb;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	
	
}
