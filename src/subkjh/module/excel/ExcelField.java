package subkjh.module.excel;

import java.lang.reflect.Method;

public class ExcelField {

	int index;

	Method method;

	public ExcelField()
	{
		
	}
	
	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public Method getMethod() {
		return method;
	}

	public void setMethod(Method method) {
		this.method = method;
	}
	
	
}
